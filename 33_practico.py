#Diseña un programa que, a partir del valor de la base y de la altura de un triángulo
#(3 y 5 metros, respectivamente), muestre el valor de su área (en metros cuadrados).
#Recuerda que el área A de un triángulo se puede calcular a partir de la base b y la altura h como A = 1/2bh.
#(El resultado es 7.5 metros cuadrados)

base_triangulo = 3
altura_triangulo = 5
area_triangulo = 1/2 * (base_triangulo * altura_triangulo)
print ("El área de un triángulo de 3 metros de base y 5 metros de altura es: " + str (area_triangulo) + " metros cuadrados.") 